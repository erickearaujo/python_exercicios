i = input('Digite Algo: ')

print( 'O tipo primeitivo desse valor é ', type(i))
print('Só tem espaços? ', i.isspace())
print('É um número?' , i.isnumeric())
print('É alfabetico?' , i.isalpha())
print('É alfanumérico', i.isalnum())
print('Esta em maiuscula? ', i.isupper())
print('Está em minusculas? ', i.islower())


